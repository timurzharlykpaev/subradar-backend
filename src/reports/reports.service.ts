  // ════════════════════════════════════════════════════════════

  private sectionTitle(doc: any, title: string, ML: number) {
    doc.moveDown(0.5);
    doc.fontSize(13).font('Helvetica-Bold').fillColor(C.text).text(title, ML);
    doc.moveDown(0.4);
  }

  private tableHeader(doc: any, cols: { label: string; w: number }[], ML: number) {
    const y = doc.y;
    doc.rect(ML, y, cols.reduce((s, c) => s + c.w, 0), 20).fill('#EEEEF5');
    doc.fontSize(8).font('Helvetica-Bold').fillColor(C.textLight);
    let x = ML;
    cols.forEach((col) => {
      doc.text(col.label.toUpperCase(), x + 6, y + 5, { width: col.w - 12 });
      x += col.w;
    });
    doc.y = y + 22;
  }

  private tableRow(doc: any, values: string[], cols: { label: string; w: number }[], ML: number, idx: number) {
    const y = doc.y;
    const totalW = cols.reduce((s, c) => s + c.w, 0);
    if (idx % 2 === 0) doc.rect(ML, y, totalW, 18).fill(C.rowEven);
    doc.fontSize(9).font('Helvetica').fillColor(C.text);
    let x = ML;
    values.forEach((val, i) => {
      const color = i === 0 && val.match(/^\d+$/) ? C.textMuted : C.text;
      doc.fillColor(color).text(val, x + 6, y + 4, { width: cols[i].w - 12 });
      x += cols[i].w;
    });
    doc.y = y + 18;
  }

  private drawLine(doc: any, ML: number, CW: number) {
    doc.moveTo(ML, doc.y).lineTo(ML + CW, doc.y).lineWidth(0.5).strokeColor(C.border).stroke();
    doc.moveDown(0.3);
  }

