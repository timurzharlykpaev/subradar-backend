  private async fetchIcon(url: string): Promise<Buffer | null> {
    try {
      const res = await fetch(url, { signal: AbortSignal.timeout(3000) });
      if (!res.ok) return null;
      const ct = res.headers.get('content-type') || '';
      if (!ct.startsWith('image/')) return null;
      return Buffer.from(await res.arrayBuffer());
    } catch { return null; }
  }

  private monthly(sub: Subscription): number {
    const amt = Number(sub.amount) || 0;
    switch (sub.billingPeriod) {
      case 'YEARLY': return amt / 12;
      case 'QUARTERLY': return amt / 3;
      case 'WEEKLY': return amt * 4.33;
      default: return amt;
    }
  }

  private fmtDate(d?: string | Date | null, locale = 'en'): string {
    if (!d) return '—';
    const date = new Date(d);
    if (isNaN(date.getTime())) return '—';
    const loc = locale.startsWith('ru') ? 'ru-RU' : locale.startsWith('kk') ? 'kk-KZ' : 'en-US';
    return date.toLocaleDateString(loc, { year: 'numeric', month: 'short', day: 'numeric' });
  }

  private periodLabel(p: string, l = 'en'): string {
    const key = { MONTHLY: 'monthly', YEARLY: 'yearly', WEEKLY: 'weekly', QUARTERLY: 'quarterly', LIFETIME: 'lifetime', ONE_TIME: 'one_time' }[p] || p;
    return t(l, key);
  }
